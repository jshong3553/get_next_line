#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t i;
	size_t j;
	size_t result;

	i = 0;
	while (dest[i])
		++i;
	result = 0;
	while (src[result])
		result++;
	if (size <= i)
		result = result + size;
	else
		result = result + i;
	j = 0;
	while (src[j] != '\0' && i + 1 < size)
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (result);
}
