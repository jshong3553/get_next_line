#include "libft.h"

char	*ft_strcpy(char *s1, char *s2)
{
	int index;

	index = 0;
	while (s2[index])
	{
		s1[index] = s2[index];
		index++;
	}
	s1[index] = s2[index];
	return (s1);
}
